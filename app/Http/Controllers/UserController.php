    public function destroy($id)
    {
        try {
            DB::beginTransaction();

            $user = User::find($id);

            if (!$user) {
                return response()->json(['error' => 'Пользователь не найден'], 404);
            }

            $user->delete();

            // Удаление сообщений пользователя
            UserMessage::where('user_id', $id)->delete();

