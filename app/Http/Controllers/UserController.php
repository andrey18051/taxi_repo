    public function user_app()
    {
        $users = User::all();
        $user_pas_1 = self::userPas_1(); // Примеры данных в массивах
        $user_pas_4 = self::userPas_4();
        $user_pas_2 = self::userPas_2();
        foreach ($users as $user) {
            $email = $user->email;

            if (in_array($email, $user_pas_1)) {
                $user->app_pas_1 = 1;
                $user->save();
            }

            if (in_array($email, $user_pas_4)) {
                $user->app_pas_4 = 1;
                $user->save();
            }

            if (in_array($email, $user_pas_2)) {
                $user->app_pas_2 = 1;
                $user->save();
            }
        }
    }


    public function userList()
    {
        // Определяем пороговую дату (сегодняшняя дата минус 25 дней)
        $thresholdDate = Carbon::now()->subDays(25);

        // Ищем пользователей, которые не заходили после пороговой даты
        $inactiveUsers = UserVisit::where('created_at', '<=', $thresholdDate)
            ->select('user_id', 'app_name')
            ->distinct()
            ->get();

        // Возвращаем список email
        return User::whereIn('id', $inactiveUsers)
            ->where(function ($query) {
                $query->where('sent_email', '==', 1)
                    ->orWhereNull('sent_email');
            })
            ->select('email', 'app_pas_1', 'app_pas_2', 'app_pas_4')
            ->get();
    }
